		// Available for future use: HAS_TCP, HAS_UDP, HAS_ICMP, etc.
		
		// ========== TX PROTOCOL/VERSION FLAGS (bits 32-37) ==========
		long PACKET_FLAG_TX_IPV4 = 1L << 32;
		long PACKET_FLAG_TX_IPV6 = 1L << 33;
		long PACKET_FLAG_TX_OUTER_IP_CKSUM = 1L << 34;
		long PACKET_FLAG_TX_OUTER_UDP_CKSUM = 1L << 35;
		long PACKET_FLAG_TX_OUTER_IPV4 = 1L << 36;
		long PACKET_FLAG_TX_OUTER_IPV6 = 1L << 37;
		
		// ========== RESERVED (bits 38-42) ==========
		// Available for future expansion
		
		// ========== TX TUNNEL FLAGS (bits 43-52) - Individual bits ==========
		long PACKET_FLAG_TX_TUNNEL_VXLAN = 1L << 43;
		long PACKET_FLAG_TX_TUNNEL_GRE = 1L << 44;
		long PACKET_FLAG_TX_TUNNEL_IPIP = 1L << 45;
		long PACKET_FLAG_TX_TUNNEL_GENEVE = 1L << 46;
		long PACKET_FLAG_TX_TUNNEL_MPLSOUDP = 1L << 47;
		long PACKET_FLAG_TX_TUNNEL_VXLAN_GPE = 1L << 48;
