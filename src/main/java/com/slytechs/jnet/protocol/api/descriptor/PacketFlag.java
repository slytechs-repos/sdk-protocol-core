		// ========== TX OFFLOAD FLAGS (bits 53-63) ==========
		long PACKET_FLAG_TX_MACSEC = 1L << 53;
		long PACKET_FLAG_TX_SEC_OFFLOAD = 1L << 54;
		long PACKET_FLAG_TX_QINQ = 1L << 55;
		long PACKET_FLAG_TX_TCP_SEG = 1L << 56;
		long PACKET_FLAG_TX_UDP_SEG = 1L << 57;
		long PACKET_FLAG_TX_IEEE1588_TMST = 1L << 58;
